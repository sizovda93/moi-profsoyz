-- Content library for agents: posts, messages,
-- guides, CTAs — all tied to referral links.

CREATE TABLE IF NOT EXISTS public.marketing_assets (
  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  category    TEXT NOT NULL CHECK (category IN ('social', 'direct', 'howto', 'scripts')),
  type        TEXT NOT NULL CHECK (type IN ('post', 'message', 'guide', 'cta')),
  title       TEXT NOT NULL,
  description TEXT,
  body        TEXT NOT NULL,
  sort_order  INT NOT NULL DEFAULT 0,
  is_featured BOOLEAN NOT NULL DEFAULT false,
  status      TEXT NOT NULL DEFAULT 'published' CHECK (status IN ('draft', 'published', 'archived')),
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_marketing_assets_category ON marketing_assets(category);
CREATE INDEX idx_marketing_assets_status ON marketing_assets(status) WHERE status = 'published';

ALTER TABLE public.marketing_assets ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow all for dev" ON public.marketing_assets
  FOR ALL USING (true) WITH CHECK (true);

